// Convierte un teléfono local (ej. "(011) 7172-2502") en un link de WhatsApp
export function telefonoToWhatsAppUrl(telefono) {
  const digits = telefono.replace(/\D/g, '').replace(/^0/, '')
  return `https://wa.me/549${digits}`
}

